/**
 * Main theme composable
 *
 * @param darkTheme Whether to use dark theme (defaults to system setting)
 * @param dynamicColor Whether to use dynamic colors on Android 12+ (defaults to true)
 * @param content The content to theme
 */
@Composable
fun XRAiAssistantTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        // Dynamic color is available on Android 12+
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content
    )
}

/**
 * Extension properties for semantic colors
 */
val ColorScheme.success: androidx.compose.ui.graphics.Color
    get() = Success

val ColorScheme.warning: androidx.compose.ui.graphics.Color
    get() = Warning

val ColorScheme.info: androidx.compose.ui.graphics.Color
    get() = Info

val ColorScheme.codeBackground: androidx.compose.ui.graphics.Color
    get() = if (this == DarkColorScheme) CodeBackgroundDark else CodeBackground

val ColorScheme.userMessageBackground: androidx.compose.ui.graphics.Color
    get() = UserMessageBackground

val ColorScheme.userMessageText: androidx.compose.ui.graphics.Color
    get() = UserMessageText

val ColorScheme.assistantMessageBackground: androidx.compose.ui.graphics.Color
    get() = if (this == DarkColorScheme) AssistantMessageBackgroundDark else AssistantMessageBackground

val ColorScheme.assistantMessageText: androidx.compose.ui.graphics.Color
    get() = if (this == DarkColorScheme) AssistantMessageTextDark else AssistantMessageText
